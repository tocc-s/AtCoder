#include <bits/stdc++.h>
using namespace std;

int main()
{
    int A, B, i = 0;
    cin >> A >> B;

    // ここにプログラムを追記
    cout << "A:";
    while (i < A)
    {
        cout << "]";
        i++;
    };

    cout << endl;
    i = 0;

    cout << "B:";
    while (i < B)
    {
        cout << "]";
        i++;
    };
    cout << endl;
}